  section: { marginBottom: 20 },
  sectionTitle: { fontSize: 13, fontWeight: 700, color: "#4338ca", marginBottom: 8, textTransform: "uppercase", letterSpacing: 0.5 },
  label: { fontWeight: 700, color: "#374151", marginBottom: 2 },
  value: { color: "#4b5563", marginBottom: 6 },
  colorRow: { flexDirection: "row", gap: 8, marginTop: 4, marginBottom: 8 },
  colorSwatch: { width: 24, height: 24, borderRadius: 4 },
  promptBox: { backgroundColor: "#f8fafc", padding: 12, borderRadius: 6, border: "1px solid #e2e8f0", marginBottom: 10 },
