function DesignBriefPDF({ session, generated }: { session: WizardSession; generated: GenerateResponse }) {
  const { brand, inspirationSites, distillation, siteAnalyses } = session;

  return React.createElement(
    Document,
    { title: `SitePrompt Design Brief — ${brand.name}` },
    React.createElement(
      Page,
      { size: "A4", style: styles.page },
      // Header
      React.createElement(View, { style: styles.header },
        React.createElement(View, { style: styles.titleRow },
          React.createElement(View, { style: styles.logoBox }),
          React.createElement(View, null,
            React.createElement(Text, { style: styles.title }, `${brand.name} — Design Brief`),
            React.createElement(Text, { style: styles.subtitle }, `Generated by SitePrompt · ${new Date().toLocaleDateString("en-US", { year: "numeric", month: "long", day: "numeric" })}`),
          )
        )
      ),

      // Brand Section
      React.createElement(View, { style: styles.section },
        React.createElement(Text, { style: styles.sectionTitle }, "Your Brand"),
        React.createElement(Text, { style: styles.label }, "Purpose"),
        React.createElement(Text, { style: styles.value }, brand.purpose),
        React.createElement(Text, { style: styles.label }, "Claim to Fame"),
        React.createElement(Text, { style: styles.value }, brand.claimToFame),
        brand.currentSiteUrl ? React.createElement(View, null,
          React.createElement(Text, { style: styles.label }, "Current Site"),
          React.createElement(Text, { style: styles.value }, brand.currentSiteUrl)
        ) : null,
        React.createElement(Text, { style: styles.label }, "Brand Colors"),
        React.createElement(View, { style: styles.colorRow },
          ...brand.colors.map((c, i) =>
            React.createElement(View, { key: i, style: { ...styles.colorSwatch, backgroundColor: c } })
          )
        ),
        React.createElement(Text, { style: { ...styles.value, fontSize: 8, color: "#9ca3af" } }, brand.colors.join("  ·  ")),
      ),

      // Aesthetic Prefs
      React.createElement(View, { style: styles.section },
        React.createElement(Text, { style: styles.sectionTitle }, "Aesthetic Preferences"),
        React.createElement(Text, { style: styles.label }, "Overall Aesthetic"),
        React.createElement(Text, { style: styles.value }, distillation.overallAesthetic || "—"),
        React.createElement(Text, { style: styles.label }, "Primary Audience"),
        React.createElement(Text, { style: styles.value }, distillation.primaryAudience || "—"),
        React.createElement(Text, { style: styles.label }, "Emotion to Convey"),
        React.createElement(Text, { style: styles.value }, distillation.emotionToConvey || "—"),
        React.createElement(Text, { style: styles.label }, "What to Avoid"),
        React.createElement(Text, { style: styles.value }, distillation.whatToAvoid || "—"),
      ),

      // Inspiration Sites
      React.createElement(View, { style: styles.section },
        React.createElement(Text, { style: styles.sectionTitle }, "Design Inspiration"),
        ...inspirationSites.map((site, i) => {
          const analysis = siteAnalyses[i];
          return React.createElement(View, { key: i, style: { marginBottom: 12 } },
            React.createElement(Text, { style: { fontWeight: 700, color: "#374151", marginBottom: 2 } }, `${i + 1}. ${site.name} — ${site.url}`),
            React.createElement(Text, { style: styles.value }, site.whyYouLikeIt),
            analysis ? React.createElement(View, null,
              React.createElement(Text, { style: { fontSize: 9, color: "#6b7280" } },
                `Typography: ${[analysis.typography.serif && "Serif", analysis.typography.sansSerif && "Sans-serif", analysis.typography.display && "Display", analysis.typography.monospace && "Monospace"].filter(Boolean).join(", ") || "—"}${analysis.typography.notes ? ` — ${analysis.typography.notes}` : ""}`
              ),
              React.createElement(Text, { style: { fontSize: 9, color: "#6b7280" } },
                `Colors: ${[analysis.colors.light && "Light", analysis.colors.dark && "Dark", analysis.colors.vibrant && "Vibrant", analysis.colors.muted && "Muted"].filter(Boolean).join(", ") || "—"}${analysis.colors.notes ? ` — ${analysis.colors.notes}` : ""}`
              ),
              analysis.specificElements ? React.createElement(Text, { style: { fontSize: 9, color: "#6b7280" } },
                `Specific elements: ${analysis.specificElements}`
              ) : null,
            ) : null,
          );
        })
      ),

      // Design Synthesis
      React.createElement(View, { style: styles.section },
        React.createElement(Text, { style: styles.sectionTitle }, "Design Synthesis"),
        React.createElement(Text, { style: styles.value }, generated.summary),
      ),

      // Claude Design Prompts
      React.createElement(View, { style: styles.section },
        React.createElement(Text, { style: styles.sectionTitle }, "Claude Design Prompts"),
        React.createElement(View, { style: styles.promptBox },
          React.createElement(Text, { style: styles.promptLabel }, "Quick Prototype Prompt"),
          React.createElement(Text, { style: styles.promptText }, generated.quickPrompt),
        ),
        React.createElement(View, { style: styles.promptBox },
          React.createElement(Text, { style: styles.promptLabel }, "Full Design System Prompt"),
          React.createElement(Text, { style: styles.promptText }, generated.fullPrompt),
        ),
        React.createElement(View, { style: styles.promptBox },
          React.createElement(Text, { style: styles.promptLabel }, "Complete Specification Prompt"),
          React.createElement(Text, { style: styles.promptText }, generated.specPrompt),
        ),
      ),

      // CTA
      React.createElement(View, { style: styles.ctaBox },
        React.createElement(Text, { style: styles.ctaTitle }, "Ready to build? ServiceVision turns your brief into a real product."),
        React.createElement(View, { style: styles.ctaRow },
          ...[
            { tier: "Starter", price: "$999", desc: "Landing page prototype" },
            { tier: "Growth", price: "$4,999", desc: "Full site / app prototype" },
            { tier: "Enterprise", price: "$19,999", desc: "Full-stack MVP" },
          ].map((t) =>
            React.createElement(View, { key: t.tier, style: styles.ctaTier },
              React.createElement(Text, { style: styles.ctaPrice }, t.price),
              React.createElement(Text, { style: styles.ctaTierName }, t.tier),
              React.createElement(Text, { style: styles.ctaDesc }, t.desc),
            )
          )
        ),
        React.createElement(Text, { style: { color: "#c7d2fe", fontSize: 9, marginTop: 10 } },
          "Schedule a free consult: https://calendly.com/servicevision/siteprompt"
        ),
      ),

      // Footer
      React.createElement(View, { style: styles.footer },
        React.createElement(Text, null, `SitePrompt · siteprompt.servicevision.io`),
        React.createElement(Text, null, `© ${new Date().getFullYear()} ServiceVision`),
      ),
    )
  );
}

export async function POST(req: NextRequest) {
  try {
    const { session, generated } = await req.json() as { session: WizardSession; generated: GenerateResponse };
    const element = React.createElement(DesignBriefPDF, { session, generated });
    // @ts-expect-error — renderToBuffer expects DocumentProps but our component returns a Document
    const buffer = await renderToBuffer(element);
    return new NextResponse(Buffer.from(buffer) as unknown as BodyInit, {
      headers: {
        "Content-Type": "application/pdf",
        "Content-Disposition": `attachment; filename="siteprompt-brief-${session.brand.name.toLowerCase().replace(/\s+/g, "-")}.pdf"`,
      },
    });
  } catch (err) {
    console.error("PDF error:", err);
    return NextResponse.json({ error: "PDF generation failed" }, { status: 500 });
  }
}
